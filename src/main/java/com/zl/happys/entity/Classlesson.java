package com.zl.happys.entity;


public class Classlesson {

  private String lessonId;
  private String lessonName;
  private String unitId;
  private long lessonLev;
  private String lessonType;
  private long isbody;
  private String versionType;
  private String classGradeId;
  private String classId;
  private String etype;


  public String getLessonId() {
    return lessonId;
  }

  public void setLessonId(String lessonId) {
    this.lessonId = lessonId;
  }


  public String getLessonName() {
    return lessonName;
  }

  public void setLessonName(String lessonName) {
    this.lessonName = lessonName;
  }


  public String getUnitId() {
    return unitId;
  }

  public void setUnitId(String unitId) {
    this.unitId = unitId;
  }


  public long getLessonLev() {
    return lessonLev;
  }

  public void setLessonLev(long lessonLev) {
    this.lessonLev = lessonLev;
  }


  public String getLessonType() {
    return lessonType;
  }

  public void setLessonType(String lessonType) {
    this.lessonType = lessonType;
  }


  public long getIsbody() {
    return isbody;
  }

  public void setIsbody(long isbody) {
    this.isbody = isbody;
  }


  public String getVersionType() {
    return versionType;
  }

  public void setVersionType(String versionType) {
    this.versionType = versionType;
  }


  public String getClassGradeId() {
    return classGradeId;
  }

  public void setClassGradeId(String classGradeId) {
    this.classGradeId = classGradeId;
  }


  public String getClassId() {
    return classId;
  }

  public void setClassId(String classId) {
    this.classId = classId;
  }


  public String getEtype() {
    return etype;
  }

  public void setEtype(String etype) {
    this.etype = etype;
  }

}
